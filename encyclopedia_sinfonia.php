    <h3>Welcome</h3>
    <hr/>
    &nbsp;&nbsp;&nbsp;&nbsp;Welcome to the Encyclopedia Sinfonia, your resource for any and all information relating to the Alpha Rho chapter of the Phi Mu Alpha Sinfonia men's music fraternity.  If you're not a Brother, but would like to know how to become one, look no further.  If you are a Brother simply looking to learn more about the history of your chapter, look no further.
    
    <br/>
    
    <h4>Glossary</h4>
    <hr/>
    &nbsp;&nbsp;&nbsp;&nbsp;For someone who is unfamiliar with Greek life, there is a good deal of the language that may also be unfamiliar.  Let's try to make things a little clearer.
    <ul>
    
      <li><b>Potential:</b> A person who is either interested in the fraternity, or who someone in the fraternity may think would be interested.
      </li>
      
      <li><b>Rush Week:</b> A series of events that are open to anyone who may be interested in joining Phi Mu Alpha.  Here, the potentials get to meet the brothers and vice versa in settings that promote positive social interaction.
      </li>
      
      <li><b>Rushing:</b> When a potential comes to at least one rush week event, either to show interest or to learn more about the fraternity.
      </li>
      
      <li><b>Bid:</b> A formal invitation from the fraternity to pledge.  Potentials are voted upon by the full chapter to receive a bid.
      </li>
      
      <li><b>Bid Night:</b> The night on which bids are delivered. Potentials are asked to wait in their rooms for their bid to be hand-delivered.  The bid contains information on how to accept the bid, usually by coming to a specified spot within the same night.
      </li>
      
      <li><b>Installation:</b> The official starting point of the Pledge Process, this is the official start of the pledge process.  It usually happens a day or two after bid night.
      </li>
      
      <li><b>Probationary Member:</b> After a bid is accepted, the Potential becomes a Probationary Member.  They are not yet a brother, but have officially begun their journey to brotherhood.
      </li>
      
      <li><b>Pledge Process:</b> Usually spanning 10 weeks, this is an introduction to the fraternity. It is designed to ensure that all of the probationary members and brothers meet at least twice, strengthen the bonds of the pledge class, and teach the Probationary Members everything about the fraternity.
      </li>
      
      <li><b>Pledge Class:</b> All of the probationary members during a particular pledge process.
      </li>
      
      <li><b>Big/Little Brother:</b> Every probationary member is assigned a big brother who serves as a mentor to him throughout the pledge process.
      </li>
      
      <li><b>Brother:</b> Once a probationary member completes the pledge process, he is granted the title ‘brother.’  This gives him full rights and privileges within the national organization of Phi Mu Alpha.
      </li>
      
      <li><b>Chapter:</b> Refers to the body of Phi Mu Alpha at a given university and the individuals who make it up. For example, UNC hosts the Alpha Rho chapter of Phi Mu Alpha.   The chapter meets once a week, and these meetings are sometimes referred to as “Chapter.”
      </li>
      
      <li><b>Sinfonia:</b> The non-Greek name of Phi Mu Alpha.  The full name is Phi Mu Alpha Sinfonia, and brothers are often called “Sinfonians.”
      </li>
      
      <li><b>Hazing:</b> Negative actions performed in the name of ceremony or tradition.  Phi Mu Alpha is committed to a hazing-free pledge process.  More information is provided both by the UNC and the national Phi Mu Alpha websites.
      </li>
      
    </ul>
    
