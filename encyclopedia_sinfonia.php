    <h4>What We're About</h4>
    <hr/>
    &nbsp;&nbsp;&nbsp;&nbsp;Sinfonia is a Brotherhood of men bound together for mutual helpfulness.  Its purpose is not only to bring men together as friends, but also to  provide a network of brothers with whom men can commune and renew their  zeal — a support network that helps the individual to meet the  challenges that face him in his daily life. By teaching men who are  united as brothers to live their lives according to the noble virtues  set forth in the Fraternity’s Initiation Ritual, Sinfonia builds  better, stronger, broader individuals who are able to live in sympathy  and in harmony with their Fraternity brothers and their fellow man. The  Initiation Ritual of Phi Mu Alpha Sinfonia, which will mark the  transition into full membership, is one of the most meaningful and  beautiful of the Greek tradition. The dedication to the advancement of  universal Truths and brotherly association exemplified in the Ritual  are the foundation of individual fraternal friendship, chapter unity,  and national solidarity in the promotion of Sinfonia’s dual mission:  the making of men and the uplift of mankind.
    <br/><br/>
    &nbsp;&nbsp;&nbsp;&nbsp;Sinfonians share a love of music that unites them as brothers  with a common interest. The Fraternity teaches men to develop  themselves and their art, not for the sake of art itself, but as a  means of enriching the lives of others. Through a multitude of chapter,  provincial, and national events, Sinfonia offers unlimited  opportunities for performance, community music outreach, and other  activities designed to develop a true appreciation for the power of  music to uplift the soul.<br/>
    
    <h4>Demographics</h4>
    <hr/>
    &nbsp;&nbsp;&nbsp;&nbsp;Although the Brothers of the Alpha Rho chapter share a common love for music, the majority are not, in fact, music majors.  The fact that most of the Brothers are not music majors means that there is a wide range of skills available to meet the chapter's needs, as well as a diverse set of perspectives.  The chapter is comprised of majors ranging from physics to music and everything in between.  Brothers come from a variety of socioeconomic backgrounds as diverse as the Brothers' choices in career paths.<br/>
    
    <h4>Our Philosophy</h4>
    <hr/>
    
    &nbsp;&nbsp;&nbsp;&nbsp;As a student at Carolina  you have, of course, been made aware of the existence of groups of men within  the university community who bind themselves together under certain Greek  letters. Your presence here at the home of one of these groups, or  fraternities, indicates your interest in such organizations. We offer the  following definitions of what fraternity means to the men of one of these  groups at Carolina.</p>
    
    &nbsp;&nbsp;&nbsp;&nbsp;Fraternity is a voluntary association of university men  bound together by ties of brotherhood into a close-knit union which works for  the mutual benefit and advancement of each member individually and the  brotherhood as a whole with no conflict between these aims. Fraternity is the  feeling of belonging to a small, highly organized group within the  often-bewildering size and complexity of a large university community.  Fraternity is watching the ballgame together, supporting each other at gigs,  parties, playing sports, banquets, work parties, cookouts, new but enduring  friendships, pledge training, workshops, big weekends, road trips, ritual,  close cooperation, pinning, conventions, and the joy of seeing mutual goals  become realities, among other things.
    <br/><br/>
    
    &nbsp;&nbsp;&nbsp;&nbsp;Fraternity men pledge themselves to building within  themselves and their fraternity brothers an adherence to the highest ideals of  gentlemanly conduct. Fraternity is a unique but enduring experience…one  difficult to attain, but one that rewards throughout life the effort expended.
    <br/><br/>
    
    &nbsp;&nbsp;&nbsp;&nbsp;Such is fraternity for all fraternities—so is Phi Mu Alpha a  fraternity. Yet Phi Mu Alpha is also a unique concept among fraternities at Carolina—a concept that we  feel equips us in a very special way to face the challenges which a changing  university scene is offering to the fraternity system as a whole. Phi Mu Alpha  is a fraternity with an added purpose—a purpose that goes beyond the immediate  social ends of the typical fraternity. We are a music fraternity whose members  are brought together by more than a casual interest in music.
    <br/><br/>
    
    &nbsp;&nbsp;&nbsp;&nbsp;We build a close-knit group of university men whose mutual  interests in music are encouraged and made more vigorous by close association  with others who share these interests. Phi Mu Alpha is thus fraternity, and  Alpha Rho is Carolina fraternity, with the cooperation, parties, work, fun, and  brotherhood—yet Phi Mu Alpha chooses to direct the enthusiasm which the  experience of brotherhood builds within its members beyond itself into the  greater university community by promoting music and musical events of all types  to enrich the lives of many through the brotherhood of students of music.
    <br/><br/>
    
      &nbsp;&nbsp;&nbsp;&nbsp;To those  wishing to pledge Phi Mu Alpha, we want you to know that Alpha Rho has a proud  tradition of producing great brothers. From Andy Griffith to Woody Durham (the  voice of the Tar Heels) to Cal Horton (the town manager of Chapel   Hill), Alpha Rho has always stood for the best. We, as a chapter,  do not accept second-rate effort and that is what you must live up to. But, you  will not be alone. Our chapter will stand beside you—offering everything that  we have in order to make you a more successful brother, colleague, friend, and  student. This is the essence of Alpha Rho. You have much to strive for and it  will not be easy. But, we will be there for you. Always remember, if you take  nothing away from pledging other than this: a brother is always there for you.  We sincerely wish you the best and look forward to one day calling you a  brother of Phi Mu Alpha, Alpha Rho.
    
    
    
    
    
    
    
    
    
