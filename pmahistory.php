    <p>Phi Mu Alpha Sinfonia began with an invitation to a &quot;get-acquainted&quot; reception, extended by the &quot;Old Boys&quot; to the &quot;New Boys&quot; of Boston's New England Conservatory early in the fall term of 1898. A spontaneous discussion about forming a men's music club took place among some of the men who attended the reception and, there being considerable intrest in the idea, a meeting was planned for the evening of October 6 to further explore the possibilities.</p>
    <p> A similar desire for fellowship among musicians had earlier led Ossian E. Mills, then bursar of the Conservatory, to invite a group of male students to meet informally with him once a week. It is safe to suppose that some of these men were present at the gathering on October 6 and that through them, Mills influenced the adoption of high ideals of brotherhood by Phi Mu Alpha even before its official beginnings.</p>
    <p> The minutes of the first meeting on October 6, 1898 describe the appointment of a &quot;committee on rules and regulations,&quot; which was to prepare a set of bylaws for the new organization. On October 25, the club's 13 active and one honorary member (Ossian Mills) accepted from a committee a governing document which has remained the Fraternity's philosophy of existence to the present day. In part it read: </p>
    <p> &quot;The object of this Fraternity shall be for the development of the best and truest fraternal spirit; the mutual welfare and brotherhood of musical students; the advancement of music in America and a loyalty to the Alma Mater...&quot; </p>
    <p> The club also accepted the suggestion of the newly-elected director of the conservatory (and the Fraternity's second honorary member), George W. Chadwick, that the group adopt the name of an organization of which he had been a member during his student days in Leipzig. SINFONIA was born. </p>
    <p> The fledging society was a success from its very beginning. The first recorded initiation of new members took place on November 28, 1898, barely a month after Sinfonia's founding. Under the leadership of its first president, Frank Leslie Stone, the Fraternity carried on a busy schedule of social events, presented recitals, concerts, and shows, sponsored a Men's Glee Club, entertained visiting artists, renovated the chapter rooms set aside for their use by the Conservatory, and held regular fortnightly meetings, one of the main features of which was the initiation of new members.<!-- end #footer -->
